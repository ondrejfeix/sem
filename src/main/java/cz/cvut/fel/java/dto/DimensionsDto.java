package cz.cvut.fel.java.dto;

public class DimensionsDto {
    public float width;
    public float height;
    public float x;
    public float y;

    public DimensionsDto(float width, float height, float x, float y) {
        this.width = width;
        this.height = height;
        this.x = x;
        this.y = y;
    }
}
